    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        String cedula = txtCedula.getText();
	String nombre = txtNombre.getText();
	String apellido = txtApellido.getText();
	String correo = txtCorreo.getText();
	String pass = new String(txtPass.getPassword());
        LocalDate fecha = LocalDate.parse(txtFecha.getText(), DateTimeFormatter.ofPattern("dd/MM/yyyy"));
	if(controladorPersona.crear(cedula, nombre, apellido, fecha, correo, pass)){
	    JOptionPane.showMessageDialog(this, "Persona Registrado", "Mensaje", JOptionPane.INFORMATION_MESSAGE);
	    limpiar();
            this.dispose();
	}else
	    JOptionPane.showMessageDialog(this, "Error en los datos del usuario", "Error", JOptionPane.ERROR_MESSAGE);
    }//GEN-LAST:event_btnRegistrarActionPerformed

